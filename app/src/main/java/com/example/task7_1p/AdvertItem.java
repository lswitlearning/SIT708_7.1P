package com.example.task7_1p;

public class AdvertItem {

    private String postType;
    private String name;

    public AdvertItem(String postType, String name) {
        this.postType = postType;
        this.name = name;
    }

    public String getPostType() {
        return postType;
    }

    public String getName() {
        return name;
    }
}
